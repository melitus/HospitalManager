    private void btnSave2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave2ActionPerformed
        try{
            conn = DbConnection.getconnection();
            if (txtID.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter nurse id","Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            if (txtName.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter name","Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            if (cmbCategory.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Please select category","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtAddress.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter address","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtContactNo.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter contact no.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (txtQualifications.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter qualifications","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (cmbBloodGroup.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Please select blood group","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (txtDateOfJoining.getDate().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter joining date","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Statement stmt;
            stmt= conn.createStatement();
            String sql1="Select NurseID from nurse where NurseID= '" + txtID.getText() + "'";
            rst=stmt.executeQuery(sql1);
            if(rst.next()){
                JOptionPane.showMessageDialog( this, "Nurse ID already exists","Error", JOptionPane.ERROR_MESSAGE);
                txtID.setText("");
                txtID.requestDefaultFocus();
                return;
            }
            String d1  = ((JTextField)txtDateOfJoining.getDateEditor().getUiComponent()).getText(); 
            
            String sql= "insert into nurse (NurseID,Surname,Firstname,Category,Address,ContactNo,EmmailAddres,Qualifications,BloodGroup,DateofJoining) values(?,?,?,?,?,?,?,?,?,?)";
            
            pst=conn.prepareStatement(sql);
            
             pst.setString(1, txtID.getText());
            pst.setString(2, txtName.getText());
            pst.setString(3, jTextField1.getText());
                    
            pst.setString(4, String.valueOf(cmbCategory.getSelectedItem()));
             pst.setString(5, txtAddress.getText());
              pst.setString(6, txtContactNo.getText());
              pst.setString(7, txtEmailID.getText());
               pst.setString(8, txtQualifications.getText());
               pst.setString(9, String.valueOf(cmbBloodGroup.getSelectedItem()));
                pst.setString(10, d1);
                 
                     
             pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Successfully saved","Record",JOptionPane.INFORMATION_MESSAGE);
            btnSave.setEnabled(false);

        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnSave2ActionPerformed

